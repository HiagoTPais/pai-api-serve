<h1>CONTRATO DE ASSISTÊNCIA PÓSTUMO FAMILIAR</h1> 

<p>
Pelo presente instrumento particular de contrato de prestação de serviços, tendo por um lado como pessoa jurídica qualificada abaixo, neste ato
representado por seu diretor responsável que subscreve, doravante CONTRATADA, e do outro lado como CONTRANTE, qualificado na ficha de
inscrição que é parte integrante deste contrato, na melhor forma de direito, resolvem firmar CONTRATO DE PLANO ASSISTÊNCIA FUNERÁRIA, nos
termos da LEI 13.261/2016, que se regenera em conformidade as seguintes cláusulas e condições abaixo. A CONTRATADA compromete-se a
fornecer ao CONTRATANTE e seus BENEFICIÁRIOS, mediante as condições estabelecidas neste contrato, o serviço de assistência funerária familiar
taxativamente descrito na cláusula de 1.4, de acordo com o plano escolhido.
</p>

<p>
1. DO OBJETO
</p>

<p>
1.1 O objeto do presente instrumento consiste em um PLANO DE ASSISTÊNCIA DE SERVIÇOS FUNERÁRIOS com a previsão de prestação de
serviços de assistência póstuma ao CONTRATANTE e/ou aos seus beneficiários. Cujo valor constará na ficha de inscrição.
</p>

<p>
1.2 Para fins de prestação dos serviços descritos nesta cláusula, abrangerá as seguintes cidades: Guanambi, Caetité, Candiba, Pindaí, Palmas de
Monte Alto, Matina, Urandi e Licínio de Almeida, todas elas no Estado da Bahia.
</p>

<p>
1.3 A CONTRATADA prestará os serviços mencionados nesta cláusula em cemitérios de livre escolha do CONTRATANTE, por via terrestre, numa
distância de até 50Km. E havendo necessidade de trasladar o ente falecido para local fora da área de atendimento, ao CONTRATANTE será cobrado
pelo transporte que exceder o limite especificado, em conformidade com os preços vigentes ao tempo do óbito.
</p>

<p>
1.4 Dos itens compreendidos no serviço funerário serão ofertados 3 (três) modelos de planos (Essencial, Companheiro e Provedor) cujo valor
individual constará na ficha de inscrição, conforme a escolha do contratante.
</p>

<p>
1.4.1 PLANO ESSENCIAL (ESSN)
</p>

<p>
Cobertura de 4 pessoas: o(a) CONTRANTATE até 70 anos, 1 beneficiario(a) até 70 anos e 2 beneficiários(as) de até 60 anos. Atendimento funerário
com direito a urna mortuária padrão plano, alça parreira com visor facial; véu padrão; ornamentação de urna com flores naturais (conforme
disponibilidade); conjunto de vestes padrão; salão de velório por até 6 horas (conforme disponibilidade); paramentação de velório; e veículo funerário
para remoção e sepultamento agendado.
</p>

<p>
1.4.2 COMPANHEIRO (COMP)
</p>

Cobertura de 6 pessoas: o(a) CONTRANTATE até 70 anos, 2 benefciários(as) até 89 anos e 3 beneficiários(as) de até 60 anos. Atendimento funerário
com direito a urna mortuária padrão plano, alça varão com visor facial; véu padrão; ornamentação de urna com flores naturais (conforme
disponibilidade); conjunto de vestes padrão; salão de velório por até 12 horas (conforme disponibilidade); paramentação de velório; e veículo funerário
para remoção e sepultamento agendado.

<p>
1.4.3 PROVEDOR (PROV)
</p>

<p>
Cobertura de 8 pessoas: o(a) CONTRANTATE até 70 anos, 2 beneficiários(as) até 89 anos e 5 beneficiários(as) de até 60 anos. Assistência funeral
com urna mortuária padrão plano, alça varãozinho com visor facial; véu padrão; ornamentação de urna com flores naturais (conforme disponibilidade);
conjunto de vestes padrão; salão de velório por até 24 horas (conforme disponibilidade); paramentação de velório; veículo funerário para remoção e
sepultamento agendado.
</p>

<p>
1.5 Os planos poderão sofrer acréscimo de valor caso haja adição de serviços de cobertura (descrito na ficha de inscrição) e/ou quando o
CONTRATANTE ou BENEFICIÁRIOS exceder(em) o número de pessoas e/ou a idade prevista nas cláusulas 1.3 e 1.4.
</p>

<p>
2. DA VIGÊNCIA, VALORES, MANUTENÇÃO E REAJUSTE
</p>

<p>
2.1 O presente contrato inicia com o pagamento da taxa de adesão, cujo valor constará na ficha de inscrição, e tem o seu prazo fixado em 48
(quarenta e oito) meses, oportunidade que se renovará automática e sucessivamente, salvo manifestação de uma das partes em sentido contrário,
no prazo mínimo de 30 (trinta) dias do vencimento.
</p>

<p>
2.2 A contraprestação que o contratante pagará à CONTRATADA, denomina-se manutenção (mensalidade), e será por si, nesta data, o valor mensal,
somado a taxas adicionais, caso haver especificado na ficha de inscrição de acordo com o plano escolhido na cláusula 1.4.
</p>

<p>
2.3 Os pagamentos deverão ser efetuados na sede da CONTRATADA ou em outro local credenciado.
</p>

<p>
2.4 Os pagamentos atrasados estão sujeitos a multa de 2% ao mês, juros de 1% ao mês e correção monetária de acordo com os índices oficiais.
</p>

<p>
2.5 O não recebimento de qualquer instrumento de cobrança não desobriga o CONTRATANTE de efetuar o seu pagamento no prazo de vencimento,
visto que disponibilizamos atendimento presencial aos CONTRATANTES em nossos escritórios durante os dias úteis.
</p>

<p>
2.6 Os valores das mensalidades serão reajustados anualmente de acordo com a variação do IGP-M (Índice Geral de Preços do Mercado) acumulado
no último ano, ou outro índice que melhor reproduza a correção da moeda e o custeio do serviço funerário contratado.
</p>

<p>
2.7 Além da atualização prevista no item 2.6 desta cláusula, fica ressalvada a possibilidade de revisão decorrente de aumento comprovado dos custos
dos serviços contratados que venham afetar o equilíbrio contratual.
</p>

