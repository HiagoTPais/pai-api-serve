<p>
4. DA CARÊNCIA
</p>

<p>
4.1 O CONTRATANTE fica ciente e concorda que o presente contrato estabelece períodos de carência para a utilização dos serviços de assistência funerária, durante os quais a
assistência poderá não estar disponível (Cobertura de 0%), limitada (Cobertura Parcial de 50%) e Assistência Integral (Cobertura de 100%). As carências estabelecidas visam proteger o
equilíbrio financeiro do plano e garantir a sustentabilidade do sistema, além de evitar o uso indevido e oneroso dos serviços de assistência funerária.
</p>

<p>
4.2 Os períodos de carência aplicáveis serão especificados na TABELA DE CARÊNCIA (Cláusula 8) deste contrato, juntamente com os respectivos tipos de idade aos quais se aplicam.
O CONTRATANTE deverá observar atentamente os períodos de carência estabelecidos, após decorrido o período de carência fixada em no mínimo de 90 (noventa) dias, contados a partir
do pagamento da taxa de adesão, conforme descrito na ficha de inscrição, desde que também pagos os meses subsequentes até o montante de 4 parcelas (Adesão e três mensalidades).
</p>

<p>
4.3 A CONTRATADA se reserva o direito de alterar os períodos de carência mediante comunicação prévia ao CONTRATADO, observando sempre os prazos mínimos previstos.
</p>

<p>
4.4 Em nenhuma hipótese a antecipação de pagamento das mensalidades importará na redução do prazo de carência, somente o tempo determinado.
</p>

<p>
5. SUSPENSÃO E RESCISÃO
</p>

<p>
5.1 As garantias asseguradas neste contrato ficarão suspensas imediatamente em caso de calamidades públicas e desequilibrio do sistema operacional de assistência do plano, tais como
em: cataclismos, guerras civis, epidemia generalizada, pandemia, ou qualquer outro motivo advindo de casos fortuitos ou força maior, bem como por determinação dos poderes
constituídos. Ficando nessas hipóteses a CONTRATADA, isenta de qualquer reparação e/ou indenização.
</p>

<p>
5.2 Este contrato pode ser rescindido pelo CONTRATANTE, desde que esteja rigorosamente em dia com os pagamentos, bem como tenha cumprido todas as demais cláusulas deste
instrumento.
</p>

<p>
5.3 A prestação dos serviços funerários está condicionada à pontualidade do pagamento das mensalidades. A inadimplência do CONTRATANTE superior a 90 (noventa) dias poderá
implicar na imediata suspensão por até mais 90 (noventa) dias, oportunidade, em que poderá efetuar o pagamento das parcelas, acrescido de multa, juros e correção monetária, conforme
item 2.4. Estando suspenso o contrato poderá ser automaticamente rescindido pela CONTRATADA.
</p>

<p>
5.4 No caso de rescisão por parte da CONTRATATADA, por INADIMPLÊNCIA do CONTRATANTE superior a 90 dias no pagamento das mensalidades, deverá o CONTRATANTE, caso
queira firmar um novo contrato, ficar sujeito a um novo período de carência, conforme novo contrato.
</p>

<p>
5.5 No caso de falecimento do CONTRATANTE, qualquer um dos seus BENEFICIÁRIOS legais, poderá assumir a responsabilidade do presente contrato, com transferência de todas as
obrigações e direitos decorrentes. Devendo apenas assinar um termo de atualização e sucessão do contratante previsto na ficha de inscrição.
</p>

<p>
5.6 Ocorrendo a rescisão do presente contrato, por quaisquer motivos ao contratante não caberá devolução de qualquer valor que houver pago, haja vista que houve durante a vigência
do mesmo, a garantia de cumprimento do contrato inclusive pela oferta de toda a infraestrutura de atendimento, tais como estrutura física, móveis, imóveis, automóveis, além de
colaboradores de plantão, dentre outros.
</p>

<p>
5.7 No dia do óbito do CONTRATANTE e/ou BENEFICIÁRIOS, caso não queira ou não possa utilizar o serviço funerário descrito na cláusula 1.4, ao CONTRANTE ou BENEFICIÁRIOS e
sucessores não caberá nenhuma indenização.
</p>

<p>
5.8 O CONTRATANTE que não tenha usufruído dos serviços poderá após pagamento das três primeiras mensalidades pedir a rescisão do contrato, conforme cláusula 5.2.
</p>

<p>
5.9 O CONTRATANTE que tenha usufruído do serviço de assistência funerário previsto na cláusula 1, durante o período de vigência, e decida por rescindir, ficará obrigado ao pagamento
de multa compensatória no valor à soma das mensalidades faltantes para finalizar o período de vigência, conforme cláusula 2.1.
</p>

<p>
5.10 No caso do CONTRATANTE ter usufruído dos serviços funerários e pararem de pagar as mensalidades antes de completar o período de vigência, fica a CONTRATADA autorizada
a inscrever o nome dos devedores no serviço de proteção ao crédito remanescente, além de promover a cobrança extrajudicial e/ou judicial da diferença dos débitos restantes.
</p>

<p>
5.10 O CONTRATANTE terá seu contrato imediatamente rescindido se ficar comprovada fraude na prestação do serviço para pessoa que não seja efetivamente beneficiário ou por
inexatidão das declarações de inscrição e qualificação de BENEFICIÁRIOS, conforme requisitos despendidos na cláusula 4.1.
</p>

<p>
5.11 O pedido de rescisão por parte do CONTRATANTE deverá ser solicitado pessoalmente em uma das unidades da CONTRATADA, entregando a CONTRATADA o cartão cliente.
</p>

