</p>

<p>
3.1 São considerados BENEFICÁRIOS do CONTRATANTE, desde que comprovado vínculo, pai, mãe, cônjuge e filhos devidamente registrados na
FICHA DE INSCRIÇÃO que é parte integrante deste contrato, salvo dependentes financeiros que residam no mesmo domicílio.
</p>

<p>
3.2 A inscrição dos beneficiários deverá respeitar o limite da quantidade de pessoas e idade conforme o plano informado na ficha de inscrição. Caso
haja excedente de Beneficiários(as) ou de idade máxima, serão adicionados valores, conforme previsto na cláusula 1.5.
</p>

<p>
3.3 Os beneficiários inscritos em mais de um contrato terão seu serviço prestado pelo primeiro que for solicitado, não cabendo pelas outras inscrições
qualquer indenização por estes serviços.
</p>

<p>
3.4 Será permitida a adição e/ou substituição de BENEFICIÁRIOS mediante a comprovação da condição da cláusula 3.1 e a realização do pagamento
de uma nova taxa de adesão especifica para o(s) novo(s) beneficiario(os), também sujeitos(as) a período de carência específico para o(s) novo(s)
beneficiário(os), conforme cláusula 4.
</p>

<p>
